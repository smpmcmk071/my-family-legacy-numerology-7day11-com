export const getCharacterById = (id) => {
  return BATTLE_CHARACTERS.find(c => c.id === id);
};

export const getCharactersByCategory = (category) => {
  return BATTLE_CHARACTERS.filter(c => c.category === category);
};

export const getCategoryIcon = (category) => {
  switch(category) {
    case 'biblical': return '📖';
    case 'superhero': return '🦸';
    case 'civilian': return '👔';
    case 'historical': return '🏛️';
    case 'sports': return '🏆';
    case 'rock': return '🎸';
    default: return '⭐';
  }
};

export const getCategoryColor = (category) => {
  switch(category) {
    case 'biblical': return 'from-amber-600 to-yellow-600';
    case 'superhero': return 'from-blue-600 to-red-600';
    case 'civilian': return 'from-gray-500 to-slate-600';
    case 'historical': return 'from-emerald-600 to-teal-600';
    case 'sports': return 'from-orange-500 to-red-500';
    case 'rock': return 'from-purple-600 to-pink-600';
    default: return 'from-purple-600 to-pink-600';
  }
};

// Get all unique categories
export const getAllCategories = () => {
  return [...new Set(BATTLE_CHARACTERS.map(c => c.category))];
};